package tablolar;

public class Kitaplar {

	private int no;
	private String isbn;
	private String ad;
	private int yazar_no;
	private String yayinevi;
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public int getYazarNo() {
		return yazar_no;
	}
	public void setYazarNo(int yazar_no) {
		this.yazar_no = yazar_no;
	}
	public String getYayinevi() {
		return yayinevi;
	}
	public void setYayinevi(String yayinevi) {
		this.yayinevi = yayinevi;
	}
	
	
}
