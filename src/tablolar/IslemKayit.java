package tablolar;

public class IslemKayit {
	
	private int islem_no;
	private String alim_tarih;
	private String iade_tarih;
	private int ogr_no;
	private int kitap_no;
	
	public int getIslemNo() {
		return islem_no;
	}
	public void setIslemNo(int islemNo) {
		this.islem_no = islemNo;
	}
	public String getAlimTarih() {
		return alim_tarih;
	}
	public void setAlimTarih(String alim_tarih) {
		this.alim_tarih = alim_tarih;
	}
	public String getIadeTarih() {
		return iade_tarih;
	}
	public void setIadeTarih(String iade_tarih) {
		this.iade_tarih = iade_tarih;
	}
	public int getOgrNo() {
		return ogr_no;
	}
	public void setOgrNo(int ogr_no) {
		this.ogr_no = ogr_no;
	}
	public int getKitapNo() {
		return kitap_no;
	}
	public void setKitapNo(int kitap_no) {
		this.kitap_no = kitap_no;
	}
	

}
